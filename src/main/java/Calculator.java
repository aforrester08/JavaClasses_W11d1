public class Calculator {
    private int num1;
    private int num2;

    public int add(int num1, int num2) {
        return num1 + num2;
    }

    public int sub(int num1, int num2) {
        return num1 - num2;
    }

    public int mult(int num1, int num2) {
        return num1 * num2;
    }

    public double div(int num1, int num2) {
        return num1 / num2;
    }
}
